package com.poojitha.for_each_loop;

import java.util.Arrays;

public class for_each_loop {
    public static void main(String[] args) {
        int[] num = {1, 2, 3, 4, 5};
        //for each loop//enhanced for loop
        for(int i:num){
            System.out.println(i);
        }
//limitations
        //if you want to process the elements from ending its not possible
        //if you want tp processes the specific elements in the array its not possible

    }
}
